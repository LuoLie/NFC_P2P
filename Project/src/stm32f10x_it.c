/*******************************************************************************
* Function Name  : TIM2_IRQHandler
* Description    : This function handles TIM2 global interrupt request.
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
int s=59, min=59 ,h=24;
int Gmin=0,Gsec=59;
extern void OLED_ShowSurface(void);
extern void OLED_ShowLogo(void);
extern uint8_t show_surface_flag;
void TIM2_IRQHandler(void)
{
	if (TIM_GetITStatus(TIM2, TIM_IT_Update) != RESET)
	{
		TIM_ClearITPendingBit(TIM2, TIM_IT_Update);	
		s=s-1;
		if(showView==1&&(Gmin>=0||Gsec>0)){
			Gsec=Gsec-1;
			if(Gsec==0) 
			{
				Gmin=Gmin-1;
				Gsec=59;
			}
		}
		if(s==0) 
		{
			min=min-1;
			s=59;
		}
		if(min==0) 
		{
			h=h-1;
			min=59;
		}	
		if(show_surface_flag == 1)
		{
			OLED_ShowSurface();
		}else{
	  	//OLED_ShowLogo();
		}
	}
}

