/*******************************************************************************
* Function Name  : EXTI3_IRQHandler
* Description    : This function handles External interrupt Line 3 request.
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
extern uint8_t key_flag;
extern uint8_t sleep_flag;
extern void mcu_enter_sleep();
extern void mcu_exit_sleep();
void EXTI3_IRQHandler(void)
{
	static uint32_t ticks, ticksHome;
	
	if(EXTI_GetITStatus(EXTI_Line3) != RESET)
	{
		if(EXTI->FTSR & EXTI_Line3)	
		{
			EXTI3_Configuration(EXTI_Trigger_Rising);
			ticksHome = TimerDrvGetTicks();
		}
		else
		{
			EXTI3_Configuration(EXTI_Trigger_Falling);
			ticks = TimerDrvGetTicks();
			
			if(ticks >= ticksHome)
			{
				if(ticks - ticksHome >= 2000)
				{
					sleep_flag = 1;
//					power_off();
				}
				else
				{
					key_flag++;
				}
			}
			else
			{
				if(0xffffffff - ticksHome + ticks >= 2000)
				{
					sleep_flag = 1;
//					power_off();
				}
				else 
				{
					key_flag++;
				}
			}
		}
		EXTI_ClearITPendingBit(EXTI_Line3);
	}
}

